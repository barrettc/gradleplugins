package com.breskeby.bean;

class Point {

    private int x = 0;
    private int y = 0;

    /**
     * Return the X coordinate
     */
    public int getX(){
        return x;
    }

    /**
     * Return the y coordinate
     */
    public int getY(){
        return y;
    }

    /**
     * Set the X coordinate
     */
    public void setX(int newX) {
        x = newX;
    }

    /**
     * set the y coordinate
     */
    public void setY(int newY) {
        y = newY;
    }
}
